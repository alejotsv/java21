package com.asalasdev.dominoes;

public class Domino {
    int top;
    int bottom;

    public Domino(int x, int y) {
        this.top = x;
        this.bottom = y;
    }

//    Getters
    public int getTop() {
        return top;
    }

     public int getBottom() {
        return bottom;
    }

}
